function getPhrases(species?: string | null): string[] {
  if (species) {
    const list = BY_SPECIES[species.trim()];
    if (list?.length) return [...list, ...GENERIC];
  }
  return GENERIC;
}

export function getRandomPhrase(species?: string | null): string {
  const phrases = getPhrases(species);
  return phrases[Math.floor(Math.random() * phrases.length)];
}
